// Helper function to get translation
export function t(key: string, lang: Language = 'en'): string {
  const keys = key.split('.');
  let result: Record<string, unknown> = translations;
  
  for (const k of keys) {
    if (result && typeof result === 'object' && k in result) {
      result = result[k] as Record<string, unknown>;
    } else {
      return key; // Return key if translation not found
    }
  }
  
  if (result && typeof result === 'object' && lang in result) {
    return result[lang] as string;
  }
  
  return key;
}

// Hook-friendly translation getter
export function getTranslation(section: keyof typeof translations, key: string, lang: Language = 'en'): string {
  const sectionData = translations[section];
  if (sectionData && key in sectionData) {
    const translation = sectionData[key as keyof typeof sectionData];
    if (translation && typeof translation === 'object' && lang in translation) {
      return translation[lang as keyof typeof translation] as string;
    }
  }
  return key;
}
